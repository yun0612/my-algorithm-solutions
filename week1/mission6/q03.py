"""
2차원 리스트 [[0]*3]*3으로 초기화했을 때 발생하는 문제(하나를 바꾸면 다 바뀌는 현상)를 재현해보고, 이를 해결하는 올바른 2차원 리스트 초기화 코드를 작성하세요.
"""

test = [[0] * 3] * 3
print(test)
# [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

test[0][0] = 1
print(test)
# [[1, 0, 0], [1, 0, 0], [1, 0, 0]]
# 문제: 첫 행의 첫 값만 1이 되는게 아니라, 모든 행의 첫 값이 1로 변경됨
# 이유: test 배열이 [0,0,0]이라는 행 하나를 3번 넣은 것이라, 하나의 행이 수정되면 나머지도 같은 행을 가리키므로 전체가 변경된 것처럼 보임

# 올바른 2차원 리스트 초기화 방법
# 리스트 컴프리헨션 사용 (반복할 때마다 새로운 리스트 생성)
matrix = [[0] * 3 for _ in range(3)]
print(matrix)

matrix[0][0] = 1
print(matrix)